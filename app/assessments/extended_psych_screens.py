ItemSpec = Tuple[str, str, str]  # item_id, prompt, framing

ScreenSpec = Dict[str, Any]


def _likert_class(spec: ScreenSpec) -> Type[AssessmentInstrument]:
    iid = spec["instrument_id"]
    dname = spec["display_name"]
    prefix = spec["prefix"]
    item_specs: List[ItemSpec] = spec["items"]
    opts = spec.get("options") or SOFT
    rtype = spec.get("response_type") or ResponseType.LIKERT_0_3

    class _Instrument(AssessmentInstrument):
        instrument_id = iid
        display_name = dname

        def items(self) -> List[AssessmentItem]:
            return [
                AssessmentItem(
                    iid,
                    item_id,
                    prompt,
                    rtype,
                    opts,
                    framing,
                )
                for item_id, prompt, framing in item_specs
            ]

        def score_partial(self, answers: Dict[str, int]) -> Dict[str, Any]:
            return _score(iid, prefix, answers, len(item_specs))

    _Instrument.__name__ = f"{iid.title().replace('_', '')}Instrument"
    return _Instrument


