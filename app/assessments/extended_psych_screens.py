def _build_registry() -> Tuple[Dict[str, Dict[str, Any]], Dict[str, Dict[str, Any]]]:
    domains: Dict[str, Dict[str, Any]] = {}
    profiles: Dict[str, Dict[str, Any]] = {}
    for spec in SCREEN_SPECS:
        did = spec["domain_id"]
        if did not in domains:
            domains[did] = {
                "label": spec["domain_label"],
                "school": spec["domain_school"],
                "instruments": [],
            }
        domains[did]["instruments"].append(spec["instrument_id"])
        profiles[spec["instrument_id"]] = {
            "display_name": spec["display_name"],
            "domain": did,
            "focus": spec["focus"],
            "keywords": spec["keywords"],
            "counseling_fit": spec["counseling_fit"],
        }
    return domains, profiles


