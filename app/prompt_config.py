def build_tarot_reading_user_prompt(user_story: str, cards_block: str) -> str:
    story = (user_story or "").strip() or "특별히 적지 않았지만, 지금 마음이 궁금해요."
    return (
        f"질문·상황: {story}\n\n"
        f"뽑힌 카드 (3카드 규칙):\n{cards_block}\n\n"
        "위 카드를 **과거 → 현재 → 미래** 위치 규칙으로, **가벼운 거울**로만 해석해 주세요. "
        "확정적 예언·깊은 무의식 분석은 피해 주세요."
    )
